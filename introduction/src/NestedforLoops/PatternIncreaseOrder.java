package NestedforLoops;

public class PatternIncreaseOrder {

	public static void main(String[] args) {
		
		for(int i =1; i<=1; i++) {
			for(int j=1; j<=1; j++) {
				System.out.print("* ");
			}
			System.out.println();
			for(int k=1; k<=2; k++) {
				System.out.print("* ");
		}
			System.out.println();
			for(int l=1; l<=3; l++) {
				System.out.print("* ");
	}
	}
}
}